import '/services/location.dart';
import '/services/networking.dart';

const apiKey = '28c32515d9139257119fcbcc74664a6f';
const openWeatherMapUrl = 'https://api.openweathermap.org/data/2.5/weather';

class WeatherModel {
  Future<dynamic> getCityWeather(String cityName) async {
    String _urlString = '$openWeatherMapUrl?units=metric&'
        'q=$cityName'
        '&appid=$apiKey';

    NetworkHelper networkHelper = NetworkHelper(_urlString);

    var weatherData = await networkHelper.getData();

    return weatherData;
  }

  Future<dynamic> getLocationWeather() async {
    Location location = Location();
    await location.getCurrentLocation();

    String _urlString = '$openWeatherMapUrl?units=metric&lat='
        '${location.latitude.toString()}&lon=${location.longitude.toString()}'
        '&appid=$apiKey';

    NetworkHelper networkHelper = NetworkHelper(_urlString);

    var weatherData = await networkHelper.getData();

    return weatherData;
  }

  String getWeatherIcon(int condition) {
    if (condition < 300) {
      return '🌩';
    } else if (condition < 400) {
      return '🌧';
    } else if (condition < 600) {
      return '☔️';
    } else if (condition < 700) {
      return '☃️';
    } else if (condition < 800) {
      return '🌫';
    } else if (condition == 800) {
      return '☀️';
    } else if (condition <= 804) {
      return '☁️';
    } else {
      return '🤷‍';
    }
  }

  String getMessage(int temp) {
    if (temp > 25) {
      return 'It\'s 🍦 time';
    } else if (temp > 20) {
      return 'Time for shorts and 👕';
    } else if (temp < 10) {
      return 'You\'ll need 🧣 and 🧤';
    } else {
      return 'Bring a 🧥 just in case';
    }
  }
}
